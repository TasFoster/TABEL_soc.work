"""Канон социальных услуг (форма обслуживания на дому) и сопоставление по названию.

Сопоставление между файлами (отчёт 071, шаблон, листы бесплатники/частичники) делаем
ПО НАЗВАНИЮ услуги (номера в колонках несогласованы и встречаются опечатки), поэтому
у каждой услуги — устойчивое ключевое слово. Для каждой услуги задано:
  key        — внутренний ключ;
  keyword    — подстрока названия (нормализованная), однозначно опознающая услугу;
  ud_row     — строка на листе «Услуги-Деньги», куда писать данные;
  client_col — колонка на листах Бесплатники/Частичники (None — услуга там не выводится).
"""

import re

SERVICES = [
    # key,     keyword,                                  ud_row, client_col
    ("prod",  "продуктов питания",                          7,  3),
    ("prom",  "промышленных товаров",                       8,  4),
    ("san",   "санитарии и гигиены",                        9,  5),
    ("book",  "доставка книг",                             10,  6),
    ("cook",  "приготовлении пищи",                        11,  7),
    ("water", "обеспечение водой",                         13,  8),
    ("pay",   "оплата за счет средств",                    14,  9),
    ("clean", "уборка жилых",                              20, 10),
    ("post",  "отправка за счет средств",                  21, 11),
    ("docs",  "оформлении и восстановлении документов",    30, 12),
    ("psy",   "психологической помощи",                    24, 14),
    ("teach", "обучение родственников",                    27, None),
]

UD_ROW = {k: r for k, _kw, r, _c in SERVICES}
CLIENT_COL = {k: c for k, _kw, _r, c in SERVICES}
# колонки листов клиентов в порядке вывода (как в образце): canon -> col
COL_TO_CANON = {c: k for k, _kw, _r, c in SERVICES if c is not None}


def _norm(s):
    s = str(s or "").lower().replace("ё", "е")
    s = re.sub(r"[^а-яa-z0-9 ]", " ", s)
    return re.sub(r"\s+", " ", s).strip()


_NORM_KW = [(k, _norm(kw)) for k, kw, _r, _c in SERVICES]


def canon_of(name):
    """Вернуть ключ услуги по её названию (по ключевому слову) или None."""
    n = _norm(name)
    for key, kw in _NORM_KW:
        if kw and kw in n:
            return key
    return None
